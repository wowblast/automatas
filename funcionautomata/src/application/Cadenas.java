package application;

import java.util.ArrayList;

public class Cadenas {
	String estado;
	ArrayList<String> pila;
	int posicion;
	String estado2;
	
	int numeroregla;

	public Cadenas()
	{
		
	}

	

	public String getEstado() {
		return estado;
	}

	public void setEstado(String estado) {
		this.estado = estado;
	}

	public ArrayList<String> getPila() {
		return pila;
	}

	public void setPila(ArrayList<String> pila) {
		this.pila = pila;
	}

	public int getLetraentrada() {
		return posicion;
	}

	public void setLetraentrada(int posicion) {
		this.posicion = posicion;;
	}

	public String getEstado2() {
		return estado2;
	}

	public void setEstado2(String estado2) {
		this.estado2 = estado2;
	}

	public int getNumeroregla() {
		return numeroregla;
	}

	public void setNumeroregla(int numeroregla) {
		this.numeroregla = numeroregla;
	}
	
	
	
	

}
